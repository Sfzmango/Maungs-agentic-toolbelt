#!/usr/bin/env bash
# Maung's Agentic Toolbelt вҖ” rotating hero banner.
#
# Prints ONE random "meme": a braille-art block plus a caption drawn from that
# art's own caption pool, then the wordmark. Used by install.sh / install-codex.sh
# (one-time, on install) and the SessionStart loader (once per launch).
#
# Adding a meme = add a meme_<name>() function (a caption array + its ART) and
# list it in MEMES. Color is applied ONLY on a real TTY, so piping the output
# (e.g. into the session snapshot) never injects escape codes. Fail-safe: any
# error just yields no banner вҖ” it must never block an install or a session.

# --- colour, TTY only -------------------------------------------------------
C=""; B=""; R=""
if [ -t 1 ] && command -v tput >/dev/null 2>&1 && [ "$(tput colors 2>/dev/null || echo 0)" -ge 8 ]; then
  C="$(tput setaf 3 2>/dev/null)"; B="$(tput bold 2>/dev/null)"; R="$(tput sgr0 2>/dev/null)"
fi

WORDMARK="   M A U N G ' S   A G E N T I C   T O O L B E L T"
TAGLINE="   one developer, a whole team вҖ” not just a hammer, the whole belt."

